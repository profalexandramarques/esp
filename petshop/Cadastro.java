    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // Clique do Botão OK
        String nome = txtNome.getText().toString();        
        Animal pet = new Animal(nome);
        int idade = Integer.parseInt(txtIdade.
                     getText().toString());
        pet.setIdade(idade);
        //Tipo Animal
        if(btnOK.isSelected()){
            pet.setTipoAnimal("Gato");
        } else{
            pet.setTipoAnimal("Cachorro");
        }
        String genero = cbGenero.getSelectedItem().toString();
        pet.setGenero(genero);
        String tutor = txtTutor.getText();
        pet.setTutor(tutor);
        pet.setVacinado(cxVacinado.isSelected());
        String plano = cbPlano.getSelectedItem().toString();
        pet.setPlanoSaude(plano);
        //Adiciona na lista de clientes
        Clientes.add(pet);
        System.out.println(pet.toString());
        JOptionPane.showMessageDialog(null, 
                "Bichinho cadastrado com sucesso!"); 
        limparTela();
    }//GEN-LAST:event_btnOKActionPerformed

