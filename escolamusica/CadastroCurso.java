    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        // Inserir o curso
                // Click botão Inserir
        CursoDAO cursodao = new CursoDAO();
        Curso curso = new Curso();
        curso.setNome(txtNome.getText());
        curso.setValor(Double.parseDouble(txtValor.getText()));
        curso.setCarga_horaria(Integer.parseInt(txtCargaHoraria.getText()));
        //Inserir aluno no banco de dados
        if (cursodao.inserir(curso)) {           
           int codigo = cursodao.ultimoCodigo(); 
           JOptionPane.showMessageDialog(null, 
                   "Curso inserido com sucesso!"+" Código = "+
                           String.valueOf(codigo));
           
        }else{
            System.out.println("Erro ao inserir o curso!");
        }
        limparTela();
    }//GEN-LAST:event_btnInserirActionPerformed

