    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // Alterar os dados do curso
        CursoDAO cursodao = new CursoDAO();
        Curso curso = new Curso();
        curso.setId_curso(Integer.parseInt(txtCodigo.getText()));
        curso.setNome(txtNome.getText());
        curso.setValor(Double.parseDouble(txtValor.getText()));
        curso.setCarga_horaria(Integer.parseInt(txtCargaHoraria.getText()));
        //Inserir aluno no banco de dados
        if (cursodao.alterar(curso)) {           
           JOptionPane.showMessageDialog(null, 
                   "Curso atualizado com sucesso!");
           
        }else{
            System.out.println("Erro ao alterar o curso!");
        }
        limparTela();  
        dispose();
    }//GEN-LAST:event_btnAlterarActionPerformed


