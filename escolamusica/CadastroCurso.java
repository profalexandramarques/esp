    public void setAlterar(String codigo){
        btnInserir.setVisible(false);
        btnAlterar.setVisible(true);
        lblCodigo.setVisible(true);
        txtCodigo.setVisible(true);        
        txtCodigo.setText(codigo);
        CursoDAO cursodao = new CursoDAO();        
        Curso curso = new Curso();
        curso = cursodao.consultarCurso(Integer.parseInt(codigo));
        txtNome.setText(curso.getNome());
        txtCargaHoraria.setText(String.valueOf(curso.getCarga_horaria()));
        txtValor.setText(String.valueOf(curso.getValor()));
    }

